package Student_Curd_Operation;

public class Student {
	private int s_id;
	private String s_Name;
	private int s_Age;
	private Address s_Ad;
	public int getS_id() {
		return s_id;
	}
	public void setS_id(int s_id) {
		this.s_id = s_id;
	}
	public String getS_Name() {
		return s_Name;
	}
	public void setS_Name(String s_Name) {
		this.s_Name = s_Name;
	}
	public int getS_Age() {
		return s_Age;
	}
	public void setS_Age(int s_Age) {
		this.s_Age = s_Age;
	}
	public Address getS_Ad() {
		return s_Ad;
	}
	public void setS_Ad(Address s_Ad) {
		this.s_Ad = s_Ad;
	}
	
}
